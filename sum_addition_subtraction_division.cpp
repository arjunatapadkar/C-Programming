#include<iostream>

using namespace std;

int main()
{
    int a, b;
    cout<<"enter the value of a: \n";
    cin>>a;
    cout<<"enter the value of b: \n";
    cin>>b;
    cout<<"the sum of the "<<a<< " and "<<b<<" is "<<a+b<<"\n";
    cout<<"the subtraction of the "<<a<<" and "<<b<<" is "<<a-b<<"\n";
    cout<<"the multiplication of the "<<a<<" and "<<b<<" is "<<a*b<<"\n";
    cout<<"the division of the "<<a<<" and "<<b<<" is "<<a/b<<"\n";
    cout<<"the modulus of the "<<a<<" and "<<b<<" is "<<a%b<<"\n";
    return 0;
}